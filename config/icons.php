    /**
     * Obtiene un icono SVG por su nombre
     * 
     * @param string $name Nombre del icono (ej: 'home', 'users', 'plus')
     * @param string $classes Clases CSS adicionales o estilos inline (opcional)
     * @return string Código SVG inline
     */
    public static function get($name, $classes = '') {
        // Buscar el icono
        if (!isset(self::$icons[$name])) {
            // Fallback: retornar icono genérico si no existe
            $name = 'file-text';
        }
        
        $svg = self::$icons[$name];
        
        // Inyectar clases/estilos si se proporcionan
        if (!empty($classes)) {
            // Si contiene ':', es estilo inline
            if (strpos($classes, ':') !== false) {
                $svg = str_replace('<svg ', '<svg style="' . htmlspecialchars($classes) . '" ', $svg);
            } else {
                // Son clases CSS
                $svg = str_replace('<svg ', '<svg class="' . htmlspecialchars($classes) . '" ', $svg);
            }
        }
        
        return $svg;
    }
    
    /**
     * Verifica si un icono existe
     * 
     * @param string $name Nombre del icono
     * @return bool
     */
    public static function exists($name) {
        return isset(self::$icons[$name]);
    }
    
    /**
     * Obtiene lista de todos los iconos disponibles
     * 
     * @return array
     */
    public static function getAvailable() {
        return array_keys(self::$icons);
    }
